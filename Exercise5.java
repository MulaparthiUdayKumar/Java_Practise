// Write a program to perform bitwise Left shift(<<) and right shift(>>) operation on x=10 and y=20

class Exercise5 {
  public static void main(String args[])
  {
    byte x = 10;
    byte y = 20;
    System.out.println("Bitwise left shift : x<<2 = " +(x<<2));
    System.out.println("Bitwise right shift : x>>2 = " +(x>>2));
    System.out.println("Bitwise left shift : y<<2 = " +(y<<2));
    System.out.println("Bitwise right shift : y>>2 = " +(y>>2));
  }
}